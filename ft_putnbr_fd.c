#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	k;

	if (fd < 0 || fd > 1023)
		return ;
	k = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
	}
	else if (n < 10)
	{
		k = n + 48;
		write(fd, &k, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
